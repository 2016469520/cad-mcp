"""
三居室平面设计图 — 使用 dxf-mcp 工具链生成。
展示 MCP Server 的实际应用案例。

整体尺寸: 12000 x 10200 mm
"""
import sys
sys.path.insert(0, "..")
from dxf_mcp.server import _dispatch

# ============================================================
# 1. 新建图纸 + 图层
# ============================================================
_dispatch("new_drawing", {"filename": "test_drawing.dxf"})
for name, color in [("WALL", 7), ("DIM", 5), ("TEXT", 3), ("DOOR", 4)]:
    _dispatch("add_layer", {"name": name, "color": color})

# ============================================================
# 2. 外墙
# ============================================================
for x1, y1, x2, y2 in [
    (0, 0, 12000, 0), (12000, 0, 12000, 10200),
    (0, 10200, 12000, 10200), (0, 0, 0, 10200),
]:
    _dispatch("add_line", {"x1": x1, "y1": y1, "x2": x2, "y2": y2, "layer": "WALL"})

# ============================================================
# 3. 内部竖墙
# ============================================================
for x1, y1, x2, y2 in [
    (2200, 2700, 2200, 6600), (4200, 4800, 4200, 10200),
    (4200, 0, 4200, 2700), (6400, 0, 6400, 3900),
    (8400, 3700, 8400, 10200), (10800, 3700, 10800, 6900),
]:
    _dispatch("add_line", {"x1": x1, "y1": y1, "x2": x2, "y2": y2, "layer": "WALL"})

# ============================================================
# 4. 内部横墙
# ============================================================
for x1, y1, x2, y2 in [
    (0, 2700, 2200, 2700), (4200, 3900, 8400, 3900),
    (4200, 4800, 8400, 4800), (0, 6600, 4200, 6600),
    (8400, 6900, 12000, 6900),
]:
    _dispatch("add_line", {"x1": x1, "y1": y1, "x2": x2, "y2": y2, "layer": "WALL"})

# ============================================================
# 5. 门洞示意
# ============================================================
for x1, y1, x2, y2 in [
    (1950, 0, 2250, 1000), (2100, 0, 2100, 1000),
    (4200, 7800, 4550, 8000), (10000, 6900, 10200, 7200),
    (10800, 5100, 11100, 5300), (5100, 3900, 5300, 4200),
    (2200, 4300, 2350, 4400), (2200, 4700, 2350, 4800),
]:
    _dispatch("add_line", {"x1": x1, "y1": y1, "x2": x2, "y2": y2, "layer": "DOOR"})
for dx in [5800, 6200, 6600, 7000]:
    _dispatch("add_line", {"x1": dx, "y1": 4800, "x2": dx+100, "y2": 5000, "layer": "DOOR"})

# ============================================================
# 6. 房间名称
# ============================================================
for x, y, txt, h in [
    (2100, 8400, "主卧室", 350), (10200, 8400, "次卧室 1", 320),
    (9600, 5300, "次卧室 2", 300), (1100, 4650, "厨房", 300),
    (5300, 1950, "卫生间", 300), (6300, 7500, "客厅 / 餐厅", 400),
    (2100, 1350, "玄关", 280), (7400, 4150, "过道", 250),
    (11400, 5300, "过道", 250), (3200, 6000, "走道", 250),
]:
    _dispatch("add_text", {"x": x, "y": y, "text": txt, "height": h, "layer": "TEXT"})

# ============================================================
# 7. 尺寸标注
# ============================================================
for x, y, t, h in [
    (2100, -500, "4200", 250), (5300, -500, "2200", 250),
    (7400, -500, "2000", 250), (10200, -500, "3600", 250),
    (6000, -900, "总长 12000", 300),
]:
    _dispatch("add_text", {"x": x, "y": y, "text": t, "height": h, "layer": "DIM"})

for x, y, t, h in [
    (2100, 10700, "4200", 250), (6300, 10700, "4200", 250),
    (10200, 10700, "3600", 250), (6000, 11200, "总长 12000", 300),
]:
    _dispatch("add_text", {"x": x, "y": y, "text": t, "height": h, "layer": "DIM"})

for x, y, t, h in [
    (-900, 1350, "2700", 250), (-900, 4650, "3900", 250),
    (-900, 8400, "3600", 250), (-1300, 5100, "总宽 10200", 300),
]:
    _dispatch("add_text", {"x": x, "y": y, "text": t, "height": h, "layer": "DIM"})

for x, y, t, h in [
    (12900, 1350, "2700", 250), (12900, 5300, "3200", 250),
    (12900, 8400, "3300", 250), (13400, 5100, "总宽 10200", 300),
]:
    _dispatch("add_text", {"x": x, "y": y, "text": t, "height": h, "layer": "DIM"})

# ============================================================
# 8. 保存
# ============================================================
result = _dispatch("save", {})
print(f"✅ {result['message']}")
